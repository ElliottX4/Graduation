package top.superswc.bean;

public class CarportDeviceBean {
	private int id;
	private String ip;
	private String carportNumber;
	private double x;
	private double y;
	private int isOnline;
	private int isOnMap;
	private int isHaveCar;
	private String carNumber;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getCarportNumber() {
		return carportNumber;
	}
	public void setCarportNumber(String carportNumber) {
		this.carportNumber = carportNumber;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public int getIsOnline() {
		return isOnline;
	}
	public void setIsOnline(int isOnline) {
		this.isOnline = isOnline;
	}
	public int getIsOnMap() {
		return isOnMap;
	}
	public void setIsOnMap(int isOnMap) {
		this.isOnMap = isOnMap;
	}
	public int getIsHaveCar() {
		return isHaveCar;
	}
	public void setIsHaveCar(int isHaveCar) {
		this.isHaveCar = isHaveCar;
	}
	public String getCarNumber() {
		return carNumber;
	}
	public void setCarNumber(String carNumber) {
		this.carNumber = carNumber;
	}
	
	
}
